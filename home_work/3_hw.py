def task_2(num1: int | float, num2: int | float) -> None:
    """
    Выводит наибольшее из двух произвольных чисел.
    
    Args:
        num1 (int | float): Первое число.
        num2 (int | float): Второе число.
    """
    # Используем встроенную функцию max() для нахождения наибольшего числа
    result = max(num1, num2)
    print(f"Наибольшее число: {result}")


def task_3(num1: int | float, num2: int | float) -> None:
    """
    Проверяет, отличаются ли числа на 135.
    
    Args:
        num1 (int | float): Первое число.
        num2 (int | float): Второе число.
    """
    # Используем abs() для получения абсолютной разницы (модуля числа)
    # Это учитывает случай, когда любое из чисел может быть больше
    if abs(num1 - num2) == 135:
        print("yes")
    else:
        print("No")


def task_4(month: int) -> None:
    """
    Определяет сезон года по номеру месяца.
    
    Args:
        month (int): Номер месяца от 1 до 12.
    """
    # Проверка валидности входных данных
    if not 1 <= month <= 12:
        print("Ошибка: номер месяца должен быть от 1 до 12")
        return
    
    # Определяем сезон с помощью условий
    if month in [12, 1, 2]:  # Декабрь, Январь, Февраль
        season = "зима"
    elif month in [3, 4, 5]:  # Март, Апрель, Май
        season = "весна"
    elif month in [6, 7, 8]:  # Июнь, Июль, Август
        season = "лето"
    else:  # Сентябрь, Октябрь, Ноябрь
        season = "осень"
    
    print(f"Сезон: {season}")


def task_5(num1: int | float, num2: int | float, num3: int | float) -> None:
    """
    Проверяет, все ли числа больше 10.
    
    Args:
        num1 (int | float): Первое число.
        num2 (int | float): Второе число.
        num3 (int | float): Третье число.
    """
    # Проверяем все три условия одновременно
    if num1 > 10 and num2 > 10 and num3 > 10:
        print("yes")
    else:
        print("no")


def task_6(numbers: list[int | float]) -> None:
    """
    Находит количество положительных чисел в списке.
    
    Args:
        numbers (list): Список из 5 произвольных чисел.
    """
    # Используем генератор списка для подсчета положительных чисел
    # Условие number > 0 проверяет, что число положительное
    positive_count = sum(1 for number in numbers if number > 0)
    
    print(f"Количество положительных чисел: {positive_count}")


def task_7(years: int, months: int) -> None:
    """
    Вычисляет количество дней за указанное время.
    
    Args:
        years (int): Количество лет.
        months (int): Количество месяцев.
    """
    # Проверка на неотрицательные значения
    if years < 0 or months < 0:
        print("Ошибка: количество лет и месяцев не может быть отрицательным")
        return
    
    # Общее количество месяцев
    total_months = years * 12 + months
    
    # Расчет общего количества дней (в каждом месяце 29 дней)
    total_days = total_months * 29
    
    print(f"Общее количество дней: {total_days}")


